package Ch3parameters;

public class stringManipulation {
    public static void main(String[] args) {
        String rapper = "Dr. Dre";

        System.out.println(rapper.length()); // __

        System.out.println(rapper.substring(0,2)); // __

        System.out.println(rapper.substring(4)); // __

        System.out.println(rapper.indexOf("d")); //__
    }
}
